//Task 8

let num1 = 25;
let num2 = 30;
console.log(num1 > num2); //false
console.log(num1 < num2); //true

//Task 9

let n1 = 40;
let n2 = 26;
console.log(n1 >= n2); //true
console.log(n1 <= n2); //false

//Task 10

let a = 10;
let b = "10";
console.log(a == b); //true
console.log(a === b); //false